// Прибираємо символи, які ламають синтаксис PostgREST .or() (кома,
// дужки, лапки) та ilike-джокери (% _).
function sanitize(word: string): string {
  return word.replace(/[,()"'%_*\\]/g, '').trim();
}

// Транслітерація суто російських особливостей у бік української.
// Кожне перетворення тут — надійне правило для запозичень:
//   подвоєні приголосні → одна (аккумулятор→акумулятор),
//   ы→и, э→е, ё→е, ъ→∅.
function transliterate(word: string): string {
  return word
    .replace(/([бвгджзклмнпрстфхцчшщ])\1/g, '$1') // подвоєні приголосні
    .replace(/ы/g, 'и')
    .replace(/э/g, 'е')
    .replace(/ё/g, 'е')
    .replace(/ъ/g, '');
}

// Переклад однієї основи за словником (перше співпадіння за префіксом).
// Повертаємо ЛИШЕ українську основу, без російського закінчення:
// коротший підрядок ilike знайде всі укр. форми («килим» → «килимок»,
// «килимки»; «захист» → «Захист днища»; «чохл» → «чохол», «чохли»).
function translateStem(word: string): string | null {
  for (const [ru, ua] of RU_UA_STEMS) {
    if (word.startsWith(ru)) return ua;
  }
  return null;
}

// Для одного слова повертає всі варіанти пошуку (оригінал + переклади).
function variantsForWord(word: string): string[] {
  const out = new Set<string>();
  out.add(word);

  const stem = translateStem(word);
  if (stem) out.add(stem);

  const tr = transliterate(word);
  if (tr !== word) out.add(tr);

  // Російська «и» у багатьох коренях відповідає українській «і»
  // (фильтр→фільтр, динамик→динамік). Додаємо як ще один варіант —
  // це лише розширює пошук (OR), хибних збігів практично не дає.
  const iVariant = word.replace(/и/g, 'і');
  if (iVariant !== word) out.add(iVariant);

  return [...out];
}

/**
 * Будує масив фільтрів для Supabase за пошуковим запитом.
 *
 * Кожен елемент — рядок для `.or('name.ilike.%a%,name.ilike.%b%')`:
 * усередині слова варіанти об'єднані через OR (рос/укр/трансліт),
 * а різні слова додаються окремими `.or()` викликами — PostgREST
 * поєднує кілька `.or()` через AND. Тобто мають знайтись усі слова
 * запиту (у будь-якому написанні), у будь-якому порядку.
 *
 * Приклад: "коврик в салон" знайде "Килимки в салон авто".
 */
export function buildSearchFilters(raw: string): string[] {
  const words = raw
    .split(/\s+/)
    .map(sanitize)
    .filter((w) => w.length > 0);

  return words.map((word) => {
    const variants = variantsForWord(word);
    return variants.map((v) => `name.ilike.%${v}%`).join(',');
  });
}
